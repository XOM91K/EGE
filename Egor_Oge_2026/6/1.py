t = [(10, 5, 3), (7, 8, 2), (4, 4, 4), (9, 1, 10), (6, 3, 3), (5, 5, 0), (12, 7, 5), (3, 2, 1), (8, 6, 2), (11, 9, 3), (2, 2, 2), (10, 10, 10)]
