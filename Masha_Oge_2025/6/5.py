for D in range(-100, 100):
    ct = 0
    t = [(3, 4, 2), (-1, -2, 3), (5, 5, 5), (0, 10, 4), (7, -3, 6), (2, 2, 1), (8, 1, 8), (-4, 5, 2), (6, 6, 7), (9, 0, 0)]
    for a, b, c in t:
        if not((a * b >= D or c % 2 == 0) and (not(a < 0 and b < 0))):
            ct += 1
    if ct == 3:
        print(D)