for B in range(-100, 100):
    l = [(10, 5, 3), (7, 8, 2), (4, 4, 4), (9, 1, 10), (6, 3, 3), (5, 5, 0),
    (12, 7, 5), (3, 2, 1), (8, 6, 2), (11, 9, 3), (2, 2, 2), (10, 10, 10)]
    ct = 0
    for x, y, z in l:
        if ((x + y > B and z < 5) or (x - y == z)):
            ct += 1
    if ct == 5:
        print(B, ct)