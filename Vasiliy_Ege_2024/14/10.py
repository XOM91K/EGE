for x in range(15):
    c1 = 1 * 15 ** 4 + 2 * 15 ** 3 + 3 * 15 ** 2 + x * 15 + 5
    c2 = 1 * 15 ** 4 + x * 15 ** 3 + 2 * 15 ** 2 + 3 * 15 + 3
    if (c1 + c2) % 14 == 0:
        print((c1 + c2) // 14)

for x in '0123456789abcde':
    c1 = int('123' + x + '5', 15)
