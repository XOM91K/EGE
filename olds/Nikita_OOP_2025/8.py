n = int(input())
l = []
for x in range(n):
    l.append([int(d) for d in input().split()])
ans_l = []
for x in l:
    if abs(x[0]) > abs(x[1]):
        ans_l.append((x[0], x[1]))
for x in ans_l:
    print(x)
print('left:', tuple(min(l, key=lambda d: d[0])))
print('right:', tuple(max(l, key=lambda d: d[0])))
print('top:', tuple(max(l, key=lambda d: d[1])))
print('bottom:', tuple(min(l, key=lambda d: d[1])))
